  @override
  void initState() {
    super.initState();
    // –ò–∑–Ω–∞—á–∞–ª—å–Ω–æ –ø–æ–∫–∞–∑—ã–≤–∞–µ–º –≤—Å–µ –ø—É–Ω–∫—Ç—ã
    filteredItems = List.from(firstAidItems);
  }

  /// –§–∏–ª—å—Ç—Ä—É–µ—Ç —Å–ø–∏—Å–æ–∫ –ø–æ –≤–≤–µ–¥—ë–Ω–Ω–æ–º—É –∑–∞–ø—Ä–æ—Å—É
  void _filterItems(String query) {
    setState(() {
      if (query.isNotEmpty) {
        filteredItems = firstAidItems
            .where((item) =>
            item['title']!.toLowerCase().contains(query.toLowerCase()))
            .toList();
      } else {
        filteredItems = List.from(firstAidItems);
      }
      // –ü–æ –∂–µ–ª–∞–Ω–∏—é, —Å–æ—Ä—Ç–∏—Ä—É–µ–º –ø–æ –Ω–∞–∑–≤–∞–Ω–∏—é (–Ω–∞ —Å–ª—É—á–∞–π, –µ—Å–ª–∏ –ø–æ—Ä—è–¥–æ–∫ –Ω–µ –∑–∞–¥–∞–Ω)
      filteredItems.sort((a, b) => a['title']!
          .toLowerCase()
          .compareTo(b['title']!.toLowerCase()));
    });
  }

  /// –ü–µ—Ä–µ–∫–ª—é—á–∞–µ—Ç —Ä–µ–∂–∏–º –ø–æ–∏—Å–∫–∞
  void _toggleSearch() {
    setState(() {
      if (_isSearching) {
        // –û—Ç–∫–ª—é—á–∞–µ–º –ø–æ–∏—Å–∫, —Å–±—Ä–∞—Å—ã–≤–∞–µ–º
        _isSearching = false;
        searchController.clear();
        filteredItems = List.from(firstAidItems);
      } else {
        _isSearching = true;
      }
    });
  }

  /// –ì—Ä—É–ø–ø–∏—Ä—É–µ–º —Å–ø–∏—Å–æ–∫ –ø–æ –ø–µ—Ä–≤–æ–π –±—É–∫–≤–µ
  Map<String, List<Map<String, String>>> _groupByFirstLetter(
      List<Map<String, String>> list) {
    final Map<String, List<Map<String, String>>> grouped = {};

    for (var item in list) {
      final title = item['title'] ?? '';
      if (title.isEmpty) continue;
      // –ë–µ—Ä—ë–º –ø–µ—Ä–≤—É—é –±—É–∫–≤—É (—É—á–∏—Ç—ã–≤–∞–µ–º –∫–∏—Ä–∏–ª–ª–∏—Ü—É/–ª–∞—Ç–∏–Ω–∏—Ü—É)
      final letter = title[0].toUpperCase();
      if (!grouped.containsKey(letter)) {
        grouped[letter] = [];
      }
      grouped[letter]!.add(item);
    }

    // –°–æ—Ä—Ç–∏—Ä—É–µ–º —ç–ª–µ–º–µ–Ω—Ç—ã –≤–Ω—É—Ç—Ä–∏ –∫–∞–∂–¥–æ–π –≥—Ä—É–ø–ø—ã
    grouped.forEach((key, value) {
      value.sort((a, b) =>
          a['title']!.toLowerCase().compareTo(b['title']!.toLowerCase()));
    });

    return grouped;
  }

  /// –°—Ç—Ä–æ–∏–º –≤–∏–¥–∂–µ—Ç—ã –Ω–∞ –æ—Å–Ω–æ–≤–µ —Å–≥—Ä—É–ø–ø–∏—Ä–æ–≤–∞–Ω–Ω—ã—Ö –¥–∞–Ω–Ω—ã—Ö
  List<Widget> _buildAlphabeticalList() {
    final grouped = _groupByFirstLetter(filteredItems);
    // –°–ø–∏—Å–æ–∫ –±—É–∫–≤
    final letters = grouped.keys.toList()..sort();

    List<Widget> widgets = [];

    for (var letter in letters) {
      // –î–æ–±–∞–≤–ª—è–µ–º –∑–∞–≥–æ–ª–æ–≤–æ–∫ (–±—É–∫–≤—É)
      widgets.add(
        Padding(
          padding: const EdgeInsets.symmetric(horizontal: 16, vertical: 8),
          child: Text(
            letter,
            style: const TextStyle(fontSize: 20, fontWeight: FontWeight.bold),
          ),
        ),
      );

      // –≠–ª–µ–º–µ–Ω—Ç—ã –≤ –≥—Ä—É–ø–ø–µ
      for (var item in grouped[letter]!) {
        final title = item['title']!;
        widgets.add(
          Card(
            margin: const EdgeInsets.symmetric(vertical: 4, horizontal: 16),
            child: ListTile(
              title: Text(
                title,
                style: const TextStyle(fontWeight: FontWeight.bold),
              ),
              trailing: const Icon(Icons.arrow_forward_ios),
              onTap: () {
                Navigator.push(
                  context,
                  MaterialPageRoute(
                    builder: (context) => FirstAidDetailScreen(
                      title: title,
                      detail: item['detail']!,
                    ),
                  ),
                );
              },
            ),
          ),
        );
      }
    }

    return widgets;
  }

  @override
  void dispose() {
    searchController.dispose();
    super.dispose();
  }

  @override
  Widget build(BuildContext context) {
    final hasData = filteredItems.isNotEmpty;

    return Scaffold(
      appBar: AppBar(
        title: _isSearching
            ? TextField(
          controller: searchController,
          autofocus: true,
          decoration: const InputDecoration(
            hintText: '–ü–æ–∏—Å–∫...',
            border: InputBorder.none,
          ),
          onChanged: _filterItems,
        )
            : const Text('–ü–µ—Ä–≤–∞—è –º–µ–¥–∏—Ü–∏–Ω—Å–∫–∞—è –ø–æ–º–æ—â—å'),
        actions: [
          IconButton(
            icon: Icon(_isSearching ? Icons.clear : Icons.search),
            onPressed: _toggleSearch,
          ),
        ],
      ),
      body: hasData
          ? ListView(
        children: _buildAlphabeticalList(),
      )
          : const Center(
        child: Text('–ù–∏—á–µ–≥–æ –Ω–µ –Ω–∞–π–¥–µ–Ω–æ'),
      ),
    );
  }
}